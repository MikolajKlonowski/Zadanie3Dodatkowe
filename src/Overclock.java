public class Overclock {
    void increaseCpuClock(Computer inCPU, int oIleZwiekszyc) {
        inCPU.procesor.taktowanie = inCPU.procesor.taktowanie + oIleZwiekszyc;
    }

    void decreaseCpuClock(Computer deCPU, int oIleZmniejszyć) {
        deCPU.procesor.taktowanie = deCPU.procesor.taktowanie - oIleZmniejszyć;
    }
}
